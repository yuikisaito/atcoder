use proconio::input;

fn main() {
    input! {
        m: usize,
    }

    let mut vv = 0;
    match m {
        0..100 => {}
        100..=5000 => {
            vv = m * 10;
        }
        5001..6000 => {}
        6000..=30000 => {
            vv = m + 50;
        }
        30001..35000 => {}
        35000..=70000 => {
            vv = (m - 30) / 5 + 80;
        }
        70001..700000 => {}
        700000.. => {
            vv = 89;
        }
    }

    println!("{:02}", vv);
}
